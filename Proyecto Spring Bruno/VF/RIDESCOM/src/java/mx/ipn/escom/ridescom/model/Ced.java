public class Ced {
    int iddeporte;
    int Ciclo;

    public Ced() {
    }

    public Ced(int iddeporte) {
        this.iddeporte = iddeporte;
        this.Ciclo = Ciclo;
    }

    public int getIddeporte() {
        return iddeporte;
    }

    public void setIddeporte(int iddeporte) {
        this.iddeporte = iddeporte;
    }
    public int getCiclo() {
        return Ciclo;
    }

    public void setCiclo(int Ciclo) {
        this.Ciclo = Ciclo;
    }
    
}
